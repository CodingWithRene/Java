package classes;

public class SwitchApp {

	public static void main(String[] args) {

		int age = 22;

		switch (age) {

		case 18:
			System.out.println("You are 18");
			break;

		case 20:
			System.out.println("You are 20");
			break;

		case 21:
			System.out.println("You are 21");
			break;

		default:
			System.out.println("You are an age");

		}

	}

}
