package classes;

public class WhileApp {
	
	public static void main(String[] args) {
		
		
		int counter = 11;
		
		while(counter<=10) {
			System.out.println("Counter " + counter);
			counter++;
		}
		
		System.out.println("\nNEW THINGS \n");
		
		int counter2 = 0;
		
		do{
			System.out.println("Printing");
		}while(counter2>0);
		
		
	}

}
